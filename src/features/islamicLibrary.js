  static normalizeText(value) {
    return String(value || '').trim().toLowerCase();
  }

  static pickRandom(items) {
    if (!Array.isArray(items) || items.length === 0) {
      return null;
    }
    return items[Math.floor(Math.random() * items.length)];
  }

  static getCollection(category) {
    return this.LIBRARY[category] || null;
  }

  static getLibraryStats() {
    const byCategory = {};
    let totalItems = 0;

    Object.entries(this.LIBRARY).forEach(([key, collection]) => {
      const count = collection.items.length;
      byCategory[key] = count;
      totalItems += count;
    });

    return { totalItems, byCategory };
  }

  static getRandomLibraryItem(category) {
    const collection = this.getCollection(category);
    if (!collection) {
      return null;
    }
    return this.pickRandom(collection.items);
  }

  static async getTafsir(surahNumber, ayahNumber, tafsirSource = 'ุงูุณุนุฏู') {
    const items = this.LIBRARY.tafsir.items.filter(
      (item) => item.surah === Number(surahNumber) && item.ayah === Number(ayahNumber)
    );

    if (items.length === 0) {
      return null;
    }

    const exactSource = items.find(
      (item) => this.normalizeText(item.source) === this.normalizeText(tafsirSource)
    );

    return exactSource || items[0];
  }

  static async getHadith(category = 'all') {
    const allItems = this.LIBRARY.hadith.items;
    if (this.normalizeText(category) === 'all') {
      return this.pickRandom(allItems);
    }

    const filtered = allItems.filter(
      (item) => this.normalizeText(item.topic) === this.normalizeText(category)
    );

    return this.pickRandom(filtered.length > 0 ? filtered : allItems);
  }

  static async getQuranStory(storyName) {
    const items = this.LIBRARY.stories.items;
    if (!storyName) {
      return this.pickRandom(items);
    }

    const needle = this.normalizeText(storyName);
    return (
      items.find(
        (item) =>
          this.normalizeText(item.id) === needle || this.normalizeText(item.name).includes(needle)
      ) || null
    );
  }

  static async getSahabiBiography(name) {
    const items = this.LIBRARY.sahabi.items;
    if (!name) {
      return this.pickRandom(items);
    }

    const needle = this.normalizeText(name);
    return (
      items.find(
        (item) =>
          this.normalizeText(item.id) === needle || this.normalizeText(item.name).includes(needle)
      ) || null
    );
  }

  static async getFiqhRuling(topic) {
    const items = this.LIBRARY.fiqh.items;
    if (!topic) {
      return this.pickRandom(items);
    }

    const needle = this.normalizeText(topic);
    return (
      items.find(
        (item) =>
          this.normalizeText(item.topic).includes(needle) ||
          this.normalizeText(item.title).includes(needle)
      ) || null
    );
  }

